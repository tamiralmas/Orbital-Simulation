function Test-UsableMission([object]$Record, [object]$Cadence) {
  if ($null -eq $Record) { return $false }
  $StepSeconds = [long](Get-ObjectProperty $Record "stepSeconds")
  if ($StepSeconds -le 0 -or $StepSeconds -gt [long]$Cadence.Seconds) { return $false }
  $Samples = @(Get-ObjectProperty $Record "samples")
  if ($Samples.Count -lt 2) { return $false }
  $First = @($Samples[0])
  $Last = @($Samples[$Samples.Count - 1])
  if ($First.Count -lt 7 -or $Last.Count -lt 7) { return $false }
  try {
    $RecordStart = [long](Get-ObjectProperty $Record "startMs")
    $RecordStop = [long](Get-ObjectProperty $Record "stopMs")
    if ([long]$First[0] -ne $RecordStart -or [long]$Last[0] -ne $RecordStop) { return $false }
    $RequestedStart = [DateTimeOffset]::Parse($StartDate, $Invariant,
      [Globalization.DateTimeStyles]::AssumeUniversal).ToUnixTimeMilliseconds()
    $RequestedStop = [DateTimeOffset]::Parse($StopDate, $Invariant,
      [Globalization.DateTimeStyles]::AssumeUniversal).ToUnixTimeMilliseconds()
    return ($RecordStart -le $RequestedStart + 120000 -and $RecordStop -ge $RequestedStop - 120000)
  } catch {
    return $false
  }
}

$Trajectories = [ordered]@{}
$Failed = [Collections.Generic.List[string]]::new()
$MissionPreserved = [Collections.Generic.List[string]]::new()

if ($Mode -eq "Full") {
  if (-not (Test-Path -LiteralPath $CatalogPath)) {
    throw "Missing $CatalogPath"
  }
  $CatalogSource = [IO.File]::ReadAllText($CatalogPath)
  $Targets = [regex]::Matches($CatalogSource,
    'mission\(\s*(-?\d+),\s*"[^"]+",\s*"([A-Z_]+)"') |
    ForEach-Object {
      [pscustomobject]@{ Id = $_.Groups[1].Value; Group = $_.Groups[2].Value }
    }

  if ($Targets.Count -ne 100 -or ($Targets.Id | Select-Object -Unique).Count -ne 100) {
    throw "Expected 100 unique Horizons targets, found $($Targets.Count)."
  }

  $Index = 0
  foreach ($Target in $Targets) {
    $Index++
    $Id = $Target.Id
    $Cadence = Get-MissionCadence $Target.Group
    Write-Progress -Activity "Generating Deep 100 ephemerides" -Status "$Index / 100 (Horizons $Id)" -PercentComplete $Index
    $Rows = @()
    $FailureMessage = $null
    try {
      $Rows = @(Get-HorizonsRows $Id $Cadence.Label)
      if ($Rows.Count -le 1) { $FailureMessage = "no coverage" }
    } catch {
      $FailureMessage = $_.Exception.Message
    }
    if ($Rows.Count -gt 1) {
      $MissionRecord = [ordered]@{
        generatedAt = (Get-Date).ToUniversalTime().ToString("o")
        startMs = $Rows[0][0]
        stopMs = $Rows[$Rows.Count - 1][0]
        stepSeconds = $Cadence.Seconds
        trajectoryClass = Get-TrajectoryClass $Id
        samples = $Rows
      }
      if ($Id -eq "-55") {
        $MissionRecord.predictionStartDate = "2009-06-30"
        $MissionRecord.predictionSource = "NAIF ulysses_1990_2009_2050.bsp mission-history kernel comment"
      }
      $Trajectories[$Id] = $MissionRecord
      Write-Host ("[{0,3}/100] {1,9}  {2,5} vectors @ {3}" -f $Index, $Id, $Rows.Count, $Cadence.Label)
    } else {
      $ExistingMission = Get-ExistingMission $Id
      if (Test-UsableMission $ExistingMission $Cadence) {
        $Trajectories[$Id] = $ExistingMission
        $MissionPreserved.Add($Id)
        Write-Host ("[{0,3}/100] {1,9}  preserving existing coverage" -f $Index, $Id)
      } else {
        $Failed.Add($Id)
        Write-Host ("[{0,3}/100] {1,9}  unavailable: {2}" -f $Index, $Id, $FailureMessage)
      }
    }
    if ($DelayMs -gt 0) { Start-Sleep -Milliseconds $DelayMs }
  }
  Write-Progress -Activity "Generating Deep 100 ephemerides" -Completed

  if ($Trajectories.Count -eq 0) {
    throw "Horizons returned no usable trajectories; the existing generated bundle was not replaced."
  }

  # `unavailable` means absent from the resulting bundle, not merely absent
  # from this network attempt. Rebuild it after per-target preservation.
  $Failed.Clear()
  foreach ($Target in $Targets) {
    if (-not $Trajectories.Contains([string]$Target.Id)) { $Failed.Add([string]$Target.Id) }
  }

  $ExistingTrajectories = Get-ObjectProperty $ExistingPayload "trajectories"
  $ExistingCount = if ($null -eq $ExistingTrajectories) { 0 } else {
    @($ExistingTrajectories.PSObject.Properties).Count
  }
  $SameWindow = $null -ne $ExistingPayload -and
    [string](Get-ObjectProperty $ExistingPayload "startDate") -eq $StartDate -and
    [string](Get-ObjectProperty $ExistingPayload "stopDate") -eq $StopDate
  if ($SameWindow -and $Trajectories.Count -lt $ExistingCount) {
    throw "Mission coverage regressed from $ExistingCount to $($Trajectories.Count) for the same window; the existing generated bundle was not replaced."
  }
}

