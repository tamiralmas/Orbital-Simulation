  throw "Existing bundle has legacy JDTDB labels. Run once with -NormalizeExistingTimeScale before refreshing it."
}

function Encode([string]$Value) {
  return [Uri]::EscapeDataString($Value)
}

function Get-MissionCadence([string]$Group) {
  # Local orbiters need several samples per revolution. More slowly varying
  # heliocentric missions stay compact while still improving on daily vectors.
  switch ($Group) {
    "LUNAR"       { return [pscustomobject]@{ Label = "15 m"; Seconds = 900 } }
    "MARS"        { return [pscustomobject]@{ Label = "30 m"; Seconds = 1800 } }
    "OBSERVATORY" { return [pscustomobject]@{ Label = "3 h";  Seconds = 10800 } }
    default        { return [pscustomobject]@{ Label = "6 h";  Seconds = 21600 } }
  }
}

function Get-TrajectoryClass([string]$TargetId) {
  # Ulysses NAIF ID -55 uses the mission-history SPK through 2050, but the
  # official kernel comment says its predicted portion begins 2009-06-30.
  # Keep this narrow verified exception; Horizons coverage is not telemetry.
  if ($TargetId -eq "-55") { return "PREDICTED" }
  return "CURRENT_WINDOW"
}

function Invoke-HorizonsRows([string]$TargetId, [string]$StepSize,
    [long]$StartUtcMs, [long]$StopUtcMs) {
  $StartTdb = "JD" + (Convert-UnixMsUtcToJdTdb $StartUtcMs).ToString("F9", $Invariant)
  $StopTdb = "JD" + (Convert-UnixMsUtcToJdTdb $StopUtcMs).ToString("F9", $Invariant)
  $Query = @(
    "format=json",
    "COMMAND=$(Encode "'$TargetId'")",
    "EPHEM_TYPE=VECTORS",
    "CENTER=$(Encode "'500@10'")",
    "START_TIME=$(Encode "'$StartTdb'")",
    "STOP_TIME=$(Encode "'$StopTdb'")",
    "STEP_SIZE=$(Encode "'$StepSize'")",
    "REF_PLANE=$(Encode "'ECLIPTIC'")",
    "REF_SYSTEM=$(Encode "'ICRF'")",
    "VEC_TABLE=2",
    "VEC_CORR=$(Encode "'NONE'")",
    "OUT_UNITS=$(Encode "'KM-S'")",
    "CSV_FORMAT=YES",
    "OBJ_DATA=NO"
  ) -join "&"
  $Response = Invoke-RestMethod -Uri ("https://ssd.jpl.nasa.gov/api/horizons.api?" + $Query) -Method Get
  $Text = [string]$Response.result
  $A = $Text.IndexOf('$$SOE')
  $B = $Text.IndexOf('$$EOE')
  if ($A -lt 0 -or $B -le $A) {
    return [pscustomobject]@{ Rows = @(); Text = $Text }
  }

  $Rows = @()
  $Block = $Text.Substring($A + 5, $B - $A - 5)
  foreach ($Line in ($Block -split "`r?`n")) {
    if ($Line -notmatch '^\s*\d') { continue }
    $Part = $Line.Split(',')
    if ($Part.Count -lt 8) { continue }
    try {
      $Jd = [double]::Parse($Part[0].Trim(), $Invariant)
      $Rows += ,@(
        (Convert-JdTdbToUnixMsUtc $Jd),
        [Math]::Round([double]::Parse($Part[2].Trim(), $Invariant), 3),
        [Math]::Round([double]::Parse($Part[3].Trim(), $Invariant), 3),
        [Math]::Round([double]::Parse($Part[4].Trim(), $Invariant), 3),
        [Math]::Round([double]::Parse($Part[5].Trim(), $Invariant), 9),
        [Math]::Round([double]::Parse($Part[6].Trim(), $Invariant), 9),
        [Math]::Round([double]::Parse($Part[7].Trim(), $Invariant), 9)
      )
    } catch {
      Write-Warning "Could not parse a vector row for target $TargetId."
    }
  }
  return [pscustomobject]@{ Rows = $Rows; Text = $Text }
}

function Get-HorizonsRows([string]$TargetId, [string]$StepSize) {
  $StartUtcMs = [DateTimeOffset]::Parse($StartDate, $Invariant,
    [Globalization.DateTimeStyles]::AssumeUniversal).ToUnixTimeMilliseconds()
  $StopUtcMs = [DateTimeOffset]::Parse($StopDate, $Invariant,
    [Globalization.DateTimeStyles]::AssumeUniversal).ToUnixTimeMilliseconds()
  # Horizons rejects an entire request when either endpoint crosses the
  # loaded SPK coverage, even when most of the requested release window is
  # valid. Retry inside each source-reported boundary and retain only that
  # bounded interval; the runtime already refuses to extrapolate beyond it.
  $RetryStartUtcMs = $StartUtcMs
  $RetryStopUtcMs = $StopUtcMs
  $BoundaryMarginMs = 300000
  for ($AttemptIndex = 0; $AttemptIndex -lt 3; $AttemptIndex++) {
    $Attempt = Invoke-HorizonsRows $TargetId $StepSize $RetryStartUtcMs $RetryStopUtcMs
    if (@($Attempt.Rows).Count -ge 2) { return @($Attempt.Rows) }
    $Changed = $false
    if ($Attempt.Text -match '(?:before|prior to) A\.D\. ([0-9]{4}-[A-Z]{3}-[0-9]{2} [0-9:.]+) TDB') {
      $Boundary = [DateTimeOffset]::Parse($Matches[1], $Invariant,
        [Globalization.DateTimeStyles]::AssumeUniversal).ToUnixTimeMilliseconds()
      $NextStartUtcMs = [Math]::Max($RetryStartUtcMs, $Boundary + $BoundaryMarginMs)
      if ($NextStartUtcMs -ne $RetryStartUtcMs) { $RetryStartUtcMs = $NextStartUtcMs; $Changed = $true }
    }
    if ($Attempt.Text -match 'after A\.D\. ([0-9]{4}-[A-Z]{3}-[0-9]{2} [0-9:.]+) TDB') {
      $Boundary = [DateTimeOffset]::Parse($Matches[1], $Invariant,
        [Globalization.DateTimeStyles]::AssumeUniversal).ToUnixTimeMilliseconds()
      $NextStopUtcMs = [Math]::Min($RetryStopUtcMs, $Boundary - $BoundaryMarginMs)
      if ($NextStopUtcMs -ne $RetryStopUtcMs) { $RetryStopUtcMs = $NextStopUtcMs; $Changed = $true }
    }
    if ($RetryStartUtcMs -ge $RetryStopUtcMs -or -not $Changed) { return @() }
  }
  return @()
}

