function Test-UsableReference([object]$Record, [object]$Definition) {
  if ($null -eq $Record) { return $false }
  if ([string](Get-ObjectProperty $Record "horizonsId") -ne [string]$Definition.HorizonsId) { return $false }
  if ([long](Get-ObjectProperty $Record "stepSeconds") -ne [long]$Definition.Seconds) { return $false }
  $Samples = @(Get-ObjectProperty $Record "samples")
  if ($Samples.Count -lt 2) { return $false }
  $First = @($Samples[0])
  $Last = @($Samples[$Samples.Count - 1])
  if ($First.Count -lt 7 -or $Last.Count -lt 7) { return $false }
  try {
    $RecordStart = [long](Get-ObjectProperty $Record "startMs")
    $RecordStop = [long](Get-ObjectProperty $Record "stopMs")
    if ([long]$First[0] -ne $RecordStart -or [long]$Last[0] -ne $RecordStop) { return $false }
    $RequestedStart = [DateTimeOffset]::Parse($StartDate, $Invariant,
      [Globalization.DateTimeStyles]::AssumeUniversal).ToUnixTimeMilliseconds()
    $RequestedStop = [DateTimeOffset]::Parse($StopDate, $Invariant,
      [Globalization.DateTimeStyles]::AssumeUniversal).ToUnixTimeMilliseconds()
    return ($RecordStart -le $RequestedStart + 120000 -and $RecordStop -ge $RequestedStop - 120000)
  } catch {
    return $false
  }
}

