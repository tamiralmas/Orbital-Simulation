function Get-ObjectProperty([object]$Object, [string]$Name) {
  if ($null -eq $Object) { return $null }
  $Property = $Object.PSObject.Properties[$Name]
  if ($null -eq $Property) { return $null }
  return $Property.Value
}

# Horizons vector epochs are JDTDB. JavaScript Date values are UTC instants,
# so treating the numeric JD as a Unix/UTC timestamp makes every state about
# 69 seconds early in the current era. Keep a local leap-second table and the
# standard millisecond-level TDB-TT periodic approximation so the generated
# bundle carries real UTC instants while retaining the source vectors exactly.
$LeapSecondTable = @(
  @("1972-01-01T00:00:00Z", 10), @("1972-07-01T00:00:00Z", 11),
  @("1973-01-01T00:00:00Z", 12), @("1974-01-01T00:00:00Z", 13),
  @("1975-01-01T00:00:00Z", 14), @("1976-01-01T00:00:00Z", 15),
  @("1977-01-01T00:00:00Z", 16), @("1978-01-01T00:00:00Z", 17),
  @("1979-01-01T00:00:00Z", 18), @("1980-01-01T00:00:00Z", 19),
  @("1981-07-01T00:00:00Z", 20), @("1982-07-01T00:00:00Z", 21),
  @("1983-07-01T00:00:00Z", 22), @("1985-07-01T00:00:00Z", 23),
  @("1988-01-01T00:00:00Z", 24), @("1990-01-01T00:00:00Z", 25),
  @("1991-01-01T00:00:00Z", 26), @("1992-07-01T00:00:00Z", 27),
  @("1993-07-01T00:00:00Z", 28), @("1994-07-01T00:00:00Z", 29),
  @("1996-01-01T00:00:00Z", 30), @("1997-07-01T00:00:00Z", 31),
  @("1999-01-01T00:00:00Z", 32), @("2006-01-01T00:00:00Z", 33),
  @("2009-01-01T00:00:00Z", 34), @("2012-07-01T00:00:00Z", 35),
  @("2015-07-01T00:00:00Z", 36), @("2017-01-01T00:00:00Z", 37)
)

function Get-TaiMinusUtcSeconds([long]$UnixMs) {
  $Offset = $null
  foreach ($Entry in $LeapSecondTable) {
    $EffectiveMs = [DateTimeOffset]::Parse([string]$Entry[0], $Invariant,
      [Globalization.DateTimeStyles]::AssumeUniversal).ToUnixTimeMilliseconds()
    if ($UnixMs -lt $EffectiveMs) { break }
    $Offset = [int]$Entry[1]
  }
  if ($null -eq $Offset) {
    throw "Deep ephemeris generation supports UTC conversion from 1972 onward."
  }
  return $Offset
}

function Get-TdbMinusTtSeconds([double]$JdTt) {
  $MeanAnomalyRad = (357.53 + 0.9856003 * ($JdTt - 2451545.0)) * [Math]::PI / 180
  return 0.001657 * [Math]::Sin($MeanAnomalyRad) +
    0.00001385 * [Math]::Sin(2 * $MeanAnomalyRad)
}

function Convert-JdTdbToUnixMsUtc([double]$JdTdb) {
  $PseudoUnixMs = ($JdTdb - 2440587.5) * 86400000
  $ApproxUtcMs = [long][Math]::Round($PseudoUnixMs - 70000)
  $TaiMinusUtc = Get-TaiMinusUtcSeconds $ApproxUtcMs
  $TdbMinusUtc = $TaiMinusUtc + 32.184 + (Get-TdbMinusTtSeconds $JdTdb)
  return [long][Math]::Round($PseudoUnixMs - $TdbMinusUtc * 1000)
}

function Convert-UnixMsUtcToJdTdb([long]$UnixMs) {
  $JdUtc = 2440587.5 + $UnixMs / 86400000.0
  $TaiMinusUtc = Get-TaiMinusUtcSeconds $UnixMs
  $JdTt = $JdUtc + ($TaiMinusUtc + 32.184) / 86400.0
  return $JdUtc + ($TaiMinusUtc + 32.184 + (Get-TdbMinusTtSeconds $JdTt)) / 86400.0
}

function Set-ObjectProperty([object]$Object, [string]$Name, [object]$Value) {
  $Property = $Object.PSObject.Properties[$Name]
  if ($null -eq $Property) { $Object | Add-Member -NotePropertyName $Name -NotePropertyValue $Value }
  else { $Property.Value = $Value }
}

function Normalize-LegacyRecordTimes([object]$Record) {
  if ($null -eq $Record) { return }
  $Samples = @(Get-ObjectProperty $Record "samples")
  foreach ($Sample in $Samples) {
    if ($Sample.Count -lt 1) { continue }
    $JdTdb = 2440587.5 + [long]$Sample[0] / 86400000.0
    $Sample[0] = Convert-JdTdbToUnixMsUtc $JdTdb
  }
  if ($Samples.Count) {
    Set-ObjectProperty $Record "startMs" ([long]$Samples[0][0])
    Set-ObjectProperty $Record "stopMs" ([long]$Samples[$Samples.Count - 1][0])
  }
}

function Write-GeneratedPayload([object]$Payload) {
  $Header = @"
/* GENERATED by get_deep_ephemerides.ps1 from NASA/JPL Horizons.
 * Do not hand-edit. Positions are ephemerides/predictions, not telemetry.
 * Mission-aware cadence ranges from 15 minutes to 6 hours.
 * Source JDTDB epochs are converted to UTC instants before storage.
 * Reference bodies use bounded Sun-centered position and velocity samples.
 * Never extrapolate beyond each record's startMs/stopMs coverage. */
"use strict";
globalThis.MTP_DEEP_EPHEMERIS =
"@
  $Json = $Payload | ConvertTo-Json -Depth 9 -Compress
  $null = $Json | ConvertFrom-Json
  $TextOut = $Header + $Json + ";`n"
  $TempPath = $OutputPath + ".tmp"
  [IO.File]::WriteAllText($TempPath, $TextOut, [Text.UTF8Encoding]::new($false))
  Move-Item -LiteralPath $TempPath -Destination $OutputPath -Force
}

function Read-ExistingPayload {
  if (-not (Test-Path -LiteralPath $OutputPath)) { return $null }
  $Source = [IO.File]::ReadAllText($OutputPath)
  $Match = [regex]::Match($Source,
    '(?s)globalThis\.MTP_DEEP_EPHEMERIS\s*=\s*(\{.*\})\s*;\s*$')
  if (-not $Match.Success) {
    throw "Could not parse the existing generated bundle at $OutputPath."
  }
  return ($Match.Groups[1].Value | ConvertFrom-Json)
}

