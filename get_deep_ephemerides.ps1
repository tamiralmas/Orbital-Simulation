  $ExistingTrajectories = Get-ObjectProperty $ExistingPayload "trajectories"
  $ExistingReferences = Get-ObjectProperty $ExistingPayload "referenceBodies"
  if (-not $AlreadyUtc) {
    foreach ($Property in @($ExistingTrajectories.PSObject.Properties)) {
      Normalize-LegacyRecordTimes $Property.Value
    }
    foreach ($Property in @($ExistingReferences.PSObject.Properties)) {
      Normalize-LegacyRecordTimes $Property.Value
    }
  }
  Set-ObjectProperty $ExistingPayload "sourceTimeScale" "TDB"
  Set-ObjectProperty $ExistingPayload "sampleTimeScale" "UTC"
  Set-ObjectProperty $ExistingPayload "timeConversion" "JDTDB to UTC using leap seconds and the standard TDB-TT periodic approximation"
  Set-ObjectProperty $ExistingPayload "referenceSourceTimeScale" "TDB"
  Set-ObjectProperty $ExistingPayload "referenceSampleTimeScale" "UTC"
  $CoverageRecords = @($ExistingTrajectories.PSObject.Properties | ForEach-Object { $_.Value })
  if ($CoverageRecords.Count) {
    $CoverageStart = ($CoverageRecords | ForEach-Object { [long]$_.startMs } | Measure-Object -Minimum).Minimum
    $CoverageStop = ($CoverageRecords | ForEach-Object { [long]$_.stopMs } | Measure-Object -Maximum).Maximum
    Set-ObjectProperty $ExistingPayload "coverageStartUtc" ([DateTimeOffset]::FromUnixTimeMilliseconds($CoverageStart).UtcDateTime.ToString("o"))
    Set-ObjectProperty $ExistingPayload "coverageStopUtc" ([DateTimeOffset]::FromUnixTimeMilliseconds($CoverageStop).UtcDateTime.ToString("o"))
  }
  $ReferenceRecords = @($ExistingReferences.PSObject.Properties | ForEach-Object { $_.Value })
  if ($ReferenceRecords.Count) {
    $ReferenceStart = ($ReferenceRecords | ForEach-Object { [long]$_.startMs } | Measure-Object -Minimum).Minimum
    $ReferenceStop = ($ReferenceRecords | ForEach-Object { [long]$_.stopMs } | Measure-Object -Maximum).Maximum
    Set-ObjectProperty $ExistingPayload "referenceCoverageStartUtc" ([DateTimeOffset]::FromUnixTimeMilliseconds($ReferenceStart).UtcDateTime.ToString("o"))
    Set-ObjectProperty $ExistingPayload "referenceCoverageStopUtc" ([DateTimeOffset]::FromUnixTimeMilliseconds($ReferenceStop).UtcDateTime.ToString("o"))
  }
  Write-GeneratedPayload $ExistingPayload
  $TimeAction = if ($AlreadyUtc) { "Verified" } else { "Normalized" }
  Write-Host ($TimeAction + " JDTDB-to-UTC metadata in $OutputPath.")
  exit 0
}
