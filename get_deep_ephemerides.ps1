    $ErrorMessage = $_.Exception.Message
  }

  if ($Rows.Count -gt 1) {
    $ReferenceBodies[$BodyId] = [ordered]@{
      horizonsId = $Definition.HorizonsId
      startMs = $Rows[0][0]
      stopMs = $Rows[$Rows.Count - 1][0]
      stepSeconds = $Definition.Seconds
      samples = $Rows
    }
    Write-Host ("[ref {0,2}/{1}] {2,-10} {3,5} vectors @ {4}" -f $ReferenceIndex, $ReferenceDefinitions.Count, $BodyId, $Rows.Count, $Definition.Label)
  } else {
    $ExistingReference = Get-ExistingReference $BodyId
    if (Test-UsableReference $ExistingReference $Definition) {
      $ReferenceBodies[$BodyId] = $ExistingReference
      $ReferencePreserved.Add($BodyId)
      Write-Host ("[ref {0,2}/{1}] {2,-10} preserving existing coverage" -f $ReferenceIndex, $ReferenceDefinitions.Count, $BodyId)
    } else {
      $ReferenceFailed.Add($BodyId)
      if ($null -eq $ErrorMessage) { $ErrorMessage = "no coverage" }
      Write-Host ("[ref {0,2}/{1}] {2,-10} unavailable: {3}" -f $ReferenceIndex, $ReferenceDefinitions.Count, $BodyId, $ErrorMessage)
    }
  }
  if ($DelayMs -gt 0) { Start-Sleep -Milliseconds $DelayMs }
}
Write-Progress -Activity "Generating reference-body ephemerides" -Completed

